package pl.androidhotelbooking;

public class Room
{
    public String id;
    public String cena;


    public String getId()
    {
        return id;
    }

    public void setId(String id)
    {
        this.id = id;
    }

    public String getCena()
    {
        return cena;
    }

    public void setCena(String cena)
    {
        this.cena = cena;
    }


}
