package pl.androidhotelbooking;

public class Room
{
    public String nazwa;
    public String cena;


    public String getNazwa()
    {
        return nazwa;
    }

    public void setNazwa(String nazwa)
    {
        this.nazwa = nazwa;
    }

    public String getCena()
    {
        return cena;
    }

    public void setCena(String cena)
    {
        this.cena = cena;
    }


}
